const a = 7
let b = 3

// Sepre primeiro o sinal da OPERACAO (+, -, /, *, %) e depois o da ATRIBUIÇÃO ( = ) 

b += a // b = b + a
console.log(b)

b -= 4 // b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /= 2 // b = b / 2
console.log(b)

b %= 2 // b = b % 2    OPERAÇÃO com MODULO
console.log(b)